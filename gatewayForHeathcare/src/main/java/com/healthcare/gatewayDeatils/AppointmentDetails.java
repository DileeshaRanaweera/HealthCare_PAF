package com.healthcare.gatewayDeatils;

public class AppointmentDetails {
	
	private int appDocId;
	private String specialization;
	private String docName;
	private String hospital;
	private String date;
	private String patientNIC;
	private String patientName;
	private int patientAge;
	private String patientEmail;
	private int patientContact;
	
	
	
	public int getAppDocId() {
		return appDocId;
	}
	public void setAppDocId(int appDocId) {
		this.appDocId = appDocId;
	}
	public String getSpecialization() {
		return specialization;
	}
	public void setSpecialization(String specialization) {
		this.specialization = specialization;
	}
	public String getDocName() {
		return docName;
	}
	public void setDocName(String docName) {
		this.docName = docName;
	}
	public String getHospital() {
		return hospital;
	}
	public void setHospital(String hospital) {
		this.hospital = hospital;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPatientNIC() {
		return patientNIC;
	}
	public void setPatientNIC(String patientNIC) {
		this.patientNIC = patientNIC;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public int getPatientAge() {
		return patientAge;
	}
	public void setPatientAge(int patientAge) {
		this.patientAge = patientAge;
	}
	public String getPatientEmail() {
		return patientEmail;
	}
	public void setPatientEmail(String patientEmail) {
		this.patientEmail = patientEmail;
	}
	public int getPatientContact() {
		return patientContact;
	}
	public void setPatientContact(int patientContact) {
		this.patientContact = patientContact;
	}

}
