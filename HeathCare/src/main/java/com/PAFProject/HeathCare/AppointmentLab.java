package com.PAFProject.HeathCare;

public class AppointmentLab {
	
	private int appLabId;
	private String labName;
	private String testType;
	private String date;
	private String patientNIC;
	private String patientName;
	private int patientAge;
	private String patientEmail;
	private int patientContact;
	
	
	public int getAppLabId() {
		return appLabId;
	}
	public void setAppLabId(int appLabId) {
		this.appLabId = appLabId;
	}
	public String getLabName() {
		return labName;
	}
	public void setLabName(String labName) {
		this.labName = labName;
	}
	public String getTestType() {
		return testType;
	}
	public void setTestType(String testType) {
		this.testType = testType;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPatientNIC() {
		return patientNIC;
	}
	public void setPatientNIC(String patientNIC) {
		this.patientNIC = patientNIC;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public int getPatientAge() {
		return patientAge;
	}
	public void setPatientAge(int patientAge) {
		this.patientAge = patientAge;
	}
	public String getPatientEmail() {
		return patientEmail;
	}
	public void setPatientEmail(String patientEmail) {
		this.patientEmail = patientEmail;
	}
	public int getPatientContact() {
		return patientContact;
	}
	public void setPatientContact(int patientContact) {
		this.patientContact = patientContact;
	}
	
	
	
	
	
	
	
	

}
